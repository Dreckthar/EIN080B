        int pesos = dolares * VALOR_DOLAR;

        //Dar formato de moneda al monto en pesos 
        DecimalFormat formato = new DecimalFormat("$ #,###");
        String valorFormateado = formato.format(pesos);

        lblPesos.setText("" + valorFormateado);

